public class Plant {
    int id;
    String name;
    String swe_name;
    String latin_name;
    String type;
    String soil;
    String zone_min;
    String zone_max;
    int water;
    String misc;
    String sun;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSwe_name() {
        return swe_name;
    }

    public String getLatin_name() {
        return latin_name;
    }

    public String getType() {
        return type;
    }

    public String getSoil() {
        return soil;
    }

    public String getZone_min() {
        return zone_min;
    }

    public String getZone_max() {
        return zone_max;
    }

    public int getWater() {
        return water;
    }

    public String getMisc() {
        return misc;
    }

    public String getSun() {
        return sun;
    }
}
